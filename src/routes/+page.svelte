	<p>
		Er zijn zoveel verleidingen. Maar wat is een verleiding? Ik kan zo drie eigenschappen van een
		verleiding bedenken:
	</p>

	<ol>
		<li>Je zou het kunnen doen</li>
		<li>Het heeft je aandacht</li>
		<li>Het is slecht</li>
	</ol>

	Hmm, maar met deze definitie zou het net zo goed een "afleiding" kunnen zijn. Dus ik voeg er nog
	iets aan toe:

	<ol start="4">
		<li>Het heeft een aantrekkingskracht, een deel van je <em>wil</em> het doen</li>
	</ol>

	<p>
		Oke, laten we het ook van een andere kant benaderen: de woordstructuur (dit heet ook wel
		morfologie). Als we de "-ing" van verleiding afhalen dan hebben we verleiden. Een verleiding is
		dus hetgeen waarmee we worden verleid. En wat is dan verleiden?
	</p>

	<p>
		"ver-" geeft een verandering, vaak ten kwade aan. En "leiden" betekent "doen gaan". Verleiden is
		dus "slecht laten doen".
	</p>

	<p>Het verschil tussen 'verleiden' en 'afleiden' blijf ik moeilijk te definiÃ«ren vinden.</p>

	<p>
		De wolf overtuigt Roodkapje om mooie bloemen te plukken. Daardoor raakt ze steeds verder van het
		pad af. Bloemen zijn niet per definitie slecht, en het plukken daarvan ook niet. Maar de wolf
		gebruikt het als een middel om zorgen dat Roodkapje bezig is, terwijl hij alvast vooruitloopt om
		Roodkapjes oma te verorberen.
	</p>

	<p>
		Heeft de wolf Roodkapje afgeleid? Of verleid? Of allebei? Misschien is het afhankelijk van of
		Roodkapje iets verkeerds deed. En mochten we stellen dat van het pad afgaan iets verkeerds is,
		in hoeverre besefte ze dat, en in hoeverre had ze dat zich <em>moeten</em> beseffen?
	</p>

	<p>
		Afleiden is misschien 'van het goede pad' af laten gaan, of iemand stil doen staan. En verleiden
		is wellicht specifieker: 'het slechte pad opsturen'.
	</p>

	<p>
		En dan sluit ik af met een vraag: is er altijd <em>iemand/iets</em> die verleidt?
	</p>
